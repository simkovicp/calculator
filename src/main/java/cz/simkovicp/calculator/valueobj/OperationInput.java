package cz.simkovicp.calculator.valueobj;

public class OperationInput {

    /**
     * Operand A
     */
    private String operandA;
    /**
     * Operand B
     */
    private String operandB;

    /**
     * @return the operandA
     */
    public String getOperandA() {
        return operandA;
    }

    /**
     * @param operandA
     *            the operandA to set
     */
    public void setOperandA(String operandA) {
        this.operandA = operandA;
    }

    /**
     * @return the operandB
     */
    public String getOperandB() {
        return operandB;
    }

    /**
     * @param operandB
     *            the operandB to set
     */
    public void setOperandB(String operandB) {
        this.operandB = operandB;
    }

}
